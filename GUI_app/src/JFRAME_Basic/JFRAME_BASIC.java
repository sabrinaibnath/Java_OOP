package JFRAME_Basic;

import javax.swing.*;

public class JFRAME_BASIC {

	public static void main(String[] args) {
		JFrame frame=new JFrame();
		frame.setVisible(true);//create a frame
		frame.setTitle("HI");//sets title of frame
		frame.setSize(420, 420);//set the size
		frame.setResizable(false);//prevent frame from resized
		
	}

}
